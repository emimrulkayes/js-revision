/* while loop */
var num = 1; // loop variable
while (num <= 10) {
    console.log(num);
    num++;
}


/* For Loop  */
// console.log(num); //for debug
for (var num = 0; num < 10; num++) { //same while loop
    console.log(num);
}

/* shortcut for loop */
for (var i = 0; i < 10; i++) {
    console.log(i);
}

/* Odd Number */
for (var i = 1; i <= 20; i += 2) {
    console.log(i);
}

/* Even Number */
for (var i = 0; i <= 20; i += 2) {
    console.log(i);
}